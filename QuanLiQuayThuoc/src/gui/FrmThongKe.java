	public void updateDataThongKe() {
		deleteTable();
		ArrayList<HoaDon> listHD = hd_dao.getAllHoaDons();
		DefaultTableModel tableModal = (DefaultTableModel) tableThongKe.getModel();
		int count = 0;
		double tien = 0.0;
		for(HoaDon hd : listHD) {
			count++;
			tien+=hd.getTongTien();
			tableModal.addRow(new Object[] {hd.getMaHD(),hd.getNhanVien().getMaNV(),df.format(hd.getNgayBan()),
					hd.getTongTien()/((100-hd.getGiamGia())/100),hd.getGiamGia(),hd.getTongTien(),hd.getKhachHang().getMaKH()});	
		}
		txtSoHD.setText(count+"");
		txtTienBan.setText(tien+"");
		
	}
    


