    private void btnThongKeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThongKeActionPerformed
        // TODO add your handling code here:
    	Date ngay = dateNgay.getDate();
    	String date = df.format(ngay);
    	String nhanVien = cboMaNV.getSelectedItem().toString();
    	TableRowSorter<TableModel> sorter = new TableRowSorter<TableModel>(tableThongKe.getModel());
    	tableThongKe.setRowSorter(sorter);
    	ArrayList<HoaDon> listHD = hd_dao.getAllHoaDons();
    	int count = 0;
    	double tien = 0.0;
    	if(nhanVien.equalsIgnoreCase("Tất cả")) {
    		for(HoaDon hd : listHD) {
    			if(date.equalsIgnoreCase(df.format(hd.getNgayBan()))) {
    				count++;
    				tien += hd.getTongTien();
    			}
    		}
    		sorter.setRowFilter(RowFilter.regexFilter("(?i)"));
    		sorter.setRowFilter(RowFilter.regexFilter("(?i)" + date));
    	} 
    	else {
    		for(HoaDon hd : listHD) {
    			if(nhanVien.equals(hd.getNhanVien().getMaNV()) && date.equalsIgnoreCase(df.format(hd.getNgayBan()))) {
    				count++;
    				tien += hd.getTongTien();
    			}
    		}
    		sorter.setRowFilter(RowFilter.regexFilter("(?i)" + nhanVien));
    		sorter.setRowFilter(RowFilter.regexFilter("(?i)" + date));
    	}
    	txtSoHD.setText(count+"");
    	txtTienBan.setText(tien+"");
   
    }//GEN-LAST:event_btnThongKeActionPerformed

